# =============================================================================
# Phase 11 — Teardown bootstrap (cleanup after self-managed pivot)
# =============================================================================
phase11_teardown_bootstrap() {
  step "Phase 11: Teardown bootstrap cluster"
  if [[ ! -f "$KUBECONFIG_BOOTSTRAP" ]]; then
    log "Bootstrap kubeconfig gone — already torn down"
    return
  fi
  if kubectl --kubeconfig "$KUBECONFIG_BOOTSTRAP" \
       get cluster "$CLUSTER_NAME" -n default &>/dev/null 2>&1; then
    log "Cluster still on bootstrap — skipping teardown (pivot not complete yet?)"
    return
  fi
  nkp delete bootstrap \
    --kubeconfig "$KUBECONFIG_BOOTSTRAP" 2>/dev/null || true
  rm -f "$KUBECONFIG_BOOTSTRAP"
  log "✓ Bootstrap torn down"
}

# =============================================================================
# Phase 12 — Verify
# =============================================================================
phase12_verify() {
  step "Phase 12: Verify"
  export KUBECONFIG="$KUBECONFIG_CLUSTER"

  echo ""
  kubectl get nodes -o wide
  echo ""
  echo "Unhealthy pods (if any):"
  kubectl get pods -A --field-selector='status.phase!=Running,status.phase!=Succeeded' \
    2>/dev/null | head -20 || true

  echo ""
  log "✓ Deployment complete"
  log ""
  log "  Kubeconfig : export KUBECONFIG=$KUBECONFIG_CLUSTER"
  log "  Dashboard  : https://${CONTROL_PLANE_VIP}/dkp/kommander/dashboard"
  log ""

  local creds
  creds=$(kubectl -n kommander get secret dkp-credentials \
    -o jsonpath='{.data.password}' 2>/dev/null | base64 -d 2>/dev/null) || true
  if [[ -n "$creds" ]]; then
    log "  Admin pass : $creds"
  else
    log "  Admin pass : run 'nkp get dashboard --kubeconfig $KUBECONFIG_CLUSTER'"
  fi
}

# =============================================================================
# Main
# =============================================================================
main() {
  echo ""
  echo -e "${CYAN}${BOLD}╔══════════════════════════════════════════════════════╗"
  echo -e "║  NKP 2.17 — Air-Gapped 3-CP / 0-Worker Deploy       ║"
  echo -e "╚══════════════════════════════════════════════════════╝${NC}"
  echo ""

  phase0_preflight
  phase1_ssh_keys
  phase2_node_prereqs
  phase3_push_bundles
  phase4_bootstrap
  phase5_inventory
  phase6_create_cluster
  phase7_wait
  phase8_untaint_cp
  phase9_metallb
  phase10_kommander
  phase11_teardown_bootstrap
  phase12_verify
}

main "$@"
